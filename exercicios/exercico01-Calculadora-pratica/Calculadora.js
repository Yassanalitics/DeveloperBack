function Soma(a, b){
    return a + b
}
function Subtracao(a, b){
    return a - b
}
function Multiplicacao(a, b){
    return a * b
}
function Divisao(a, b){
    return a / b
}
function aoQuadrado(a){
    return a * a
}
function raizQuadrada(a){
    return a ** 0,5 
    //exponenciação
}

module.exports = {
Soma,
Subtracao,
Divisao,
Multiplicacao,
aoQuadrado,
raizQuadrada
}